tuple=(1)
print(type(tuple)) # give type of variable.
#but in this we can see that the type of variable is int, not tuple. 
# This is because when we create a tuple with a single element, we need to include a comma after the element to indicate that it is a tuple. 
# So, to create a tuple with a single element, we should write: as a tuple=(1,) instead of tuple=(1).
tuple=(1,)
print(type(tuple)) # Output: <class 'tuple'>