tuple=(1,2,3,4,5)
# count the number of times 1 appears in the tuple
print("The number of times 1 appears in the tuple is:",tuple.count(1)) 
# find the index of the first occurrence of 3 in the
print("The index of the first occurrence of 3 in the tuple is:",tuple.index(3)) 
 # find the maximum value in the tuple
print("The maximum value in the tuple is:",max(tuple))
 # find the minimum value in the tuple
print("The minimum value in the tuple is:",min(tuple))
 # find the length of the tuple
print("The length of the tuple is:",len(tuple))
# find the sum of all the elements in the tuple
print("The sum of all elements in the tuple is:",sum(tuple)) 
