s=set()#creating a set
a=18#creating a variable and storing a integer value in it
s.add(a)#adding the integer value to the set
print(s)#printing the set
a1='18'#creating a variable and storing a string value in it
s.add(a1)#adding the string value to the set
print(s)#printing the set after adding the string value to it. This shows that we can add both integer and string values to a set in Python, as sets can contain elements of different data types.