a=int(input("Enter first number a : "))
b=int(input("Enter second number b : "))
c=a%b                                       
print("Remainder of the two numbers is ( c ):", c)
'''this code will prompt the user to enter two numbers, 
divide the first number by the second number, 
and print the remainder. 
The input function is used to get user input, 
and the int function converts the input from a string to an integer. 
The remainder of the division is stored in variable c and printed to the console.'''